/*
 * TASK #2
 * Write a program to read two numbers from user and sum them. 
*/

#include <iostream>

using namespace std;

int main() {

    int a, b;

    cout << "Enter the first number: "; cin >> a;
    cout << "Enter the second number: "; cin >> b;
    cout << "The sum of numbers is: " << a + b << endl;

    return 0;

}